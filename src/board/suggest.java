package board;

public class suggest {
	private int suggestId;
	private String suggestTitle;
	private String userId;
	private String suggestDate;
	private String suggestContent;
	private int suggestAvailable;
	
	public int getSuggestId() {
		return suggestId;
	}
	public void setSuggestId(int suggestId) {
		this.suggestId = suggestId;
	}
	public String getSuggestTitle() {
		return suggestTitle;
	}
	public void setSuggestTitle(String suggestTitle) {
		this.suggestTitle = suggestTitle;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getSuggestDate() {
		return suggestDate;
	}
	public void setSuggestDate(String suggestDate) {
		this.suggestDate = suggestDate;
	}
	public String getSuggestContent() {
		return suggestContent;
	}
	public void setSuggestContent(String suggestContent) {
		this.suggestContent = suggestContent;
	}
	public int getSuggestAvailable() {
		return suggestAvailable;
	}
	public void setSuggestAvailable(int suggestAvailable) {
		this.suggestAvailable = suggestAvailable;
	}
	
}
