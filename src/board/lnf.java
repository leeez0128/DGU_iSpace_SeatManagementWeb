package board;

public class lnf {
	private int lnfId;
	private String lnfTitle;
	private String userId;
	private String lnfDate;
	private String lnfContent;
	private int lnfAvailable;
	private String lnfFile;
	
	public int getLnfId() {
		return lnfId;
	}
	public void setLnfId(int lnfId) {
		this.lnfId = lnfId;
	}
	public String getLnfTitle() {
		return lnfTitle;
	}
	public void setLnfTitle(String lnfTitle) {
		this.lnfTitle = lnfTitle;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getLnfDate() {
		return lnfDate;
	}
	public void setLnfDate(String lnfDate) {
		this.lnfDate = lnfDate;
	}
	public String getLnfContent() {
		return lnfContent;
	}
	public void setLnfContent(String lnfContent) {
		this.lnfContent = lnfContent;
	}
	public int getLnfAvailable() {
		return lnfAvailable;
	}
	public void setLnfAvailable(int lnfAvailable) {
		this.lnfAvailable = lnfAvailable;
	}
	public String getLnfFile() {
		return lnfFile;
	}
	public void setLnfFile(String lnfFile) {
		this.lnfFile = lnfFile;
	}
	
}
